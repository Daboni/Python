# tuple
# 리스트와 다르게 튜플은 값을 바꿀 수 없음
t1 = (1,2,'a','b')

# indexing
t1[0]           #1
t1[3]           #'b'

# slicing
t1[1:]          #(2,'a','b')

# add
t1 = (1,2,'a','b')
t2 = (3,4)
t1 + t2         #(1,2,'a','b',3,4)

# mul
t2 = (3,4)
t2 * 3          #(3,4,3,4,3,4)

# len()
t1 = (1,2,'a','b')
len(t1)         #4
